// Solicitar o nome do usuário
let nome = prompt("Insira o seu nome");

// Insira a idade do usuário
let idade = parseInt(prompt("Insira sua idade"));

// Calcular
let anosparacem = 100 - idade

// Apresentar para o usuário um alerta
alert (`${nome}, faltam ${anosparacem} anos para você completar 100 anos`);